package main;

/**
 *
 * @author Daniel
 */
public class CategorieSlug {
    private String name;
    private String slug;
    
    CategorieSlug(String name, String slug){
        this.name = name;
        this.slug = slug;
    }
    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the slug
     */
    public String getSlug() {
        return slug;
    }

    /**
     * @param slug the slug to set
     */
    public void setSlug(String slug) {
        this.slug = slug;
    }
}
